public class DivisibleBy10 {
    public static void main(String []args)
    {
        int a = 10;
        switch(a)
        {
            case 10:
            case 20:
            case 30:
            case 40:
            case 50:
            case 60:
            case 70:
            case 80:
            case 90:
            case 100:
                System.out.println("a is divisible by 10");
                break;
            default:
                System.out.println("a is not divisible by 10");
             }
    }
}
